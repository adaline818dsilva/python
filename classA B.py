class A:
         def isprime(self,a):
           for i in range[2, self.a]:
               if(self.a%i==0):
                 return False
                 return True
class B(A):
    def sod(self,a):
        self.a=a
        return sum([int(i) for i in str(self.a) ])
    
class c(B):
          def show(self):
            print("Hello")
